#include <iostream>
#include <algorithm>

int main(int choice) {
    setlocale(LC_ALL, "Russian");
    using namespace std;

    cout << "Это первое ДЗ\n";
    chgoto0:cout << "Выберите программу: ";
    cin >> choice;

    switch (choice) {
    case (1): {cout << "Мое имя\n"; break; };
        case (2): {
            double a, b;
            abgoto2:cout << "Введите A и B: ";
            if (!(cin >> a >> b)) { cout << "Не то ввел, попробуй еще раз"; goto abgoto2; };
            cout << "Сумма = " << a + b << endl;
            cout << "Разность = " << a - b << endl;
            cout << "Произведение = " << a * b << endl;
            if (b != 0) cout << "Частнoе от деления = " << a / b << endl;
            else cout << "Нельзя делить на 0!\n";
            break;
        };
        case (3): {
            double a, b;
            abgoto3:cout << "Введите A и B: ";
            if (!(cin >> a >> b)) { cout << "Не то ввел, попробуй еще раз"; goto abgoto3; };
            if (a != 0) cout << "X = " << -b / a << endl;
            else cout << "Деление на 0!";
            break;
        };
        case (4): {
            double input1, input2, input3;

            agoto4:cout << "Привет! Я решаю уравнения типа ax^2+bx+c=0 \nВведите 'a' ";
            if (!(cin >> input1)) { cout << "Не то ввел, попробуй еще раз"; goto agoto4; };
            bgoto4:cout << "Отлично! \nТеперь введите 'b' ";
            if (!(cin >> input2)) { cout << "Не то ввел, попробуй еще раз"; goto bgoto4; };
            cgoto4:cout << "Превосходно! \nОстался только 'с' ";
            if (!(cin >> input3)) { cout << "Не то ввел, попробуй еще раз"; goto cgoto4; };

            if (input1 == 0 && input2 == 0 && input3 == 0) cout << "X любой\n";
            else if ((input1 == 0 && input2 != 0 || input1 != 0 && input2 == 0) && input3 == 0) cout << 0 << endl;
            else if (input1 == 0 && input2 != 0 && input3 != 0) cout << -input3 / input2 << endl;
            else if (input1 != 0 && input2 == 0 && input3 <= 0) cout << sqrt(-input3 / input1) << " " << -sqrt(-input3 / input1) << endl;
            else if (input1 != 0 && input2 != 0 && input3 == 0) cout << input1 / input2 << " " << 0 << endl;
            else if (input1 != 0 && input2 != 0 && input3 != 0) {
                double dis = (input2 * input2) - (4 * input1 * input3);
                if (dis >= 0) {
                    if (dis != 0) cout << max((-input2 + sqrt(dis)) / (2 * input1), (-input2 - sqrt(dis)) / (2 * input1)) << " ";
                    cout << min((-input2 + sqrt(dis)) / (2 * input1), (-input2 - sqrt(dis)) / (2 * input1)) << endl;
                }
                else cout << "Отрицательный дискриминант!\n";
            }
            else cout << "Нет решения!\n";
            break;
        };
        case (5): {
            bool sun, curtain, lamp;

            sgoto5:cout << "Светло на улице? ";
            if (!(cin >> sun)) { cout << "Не то ввел, попробуй еще раз"; goto sgoto5; };
            cgoto5:cout << "Окна зашторены? ";
            if (!(cin >> curtain)) { cout << "Не то ввел, попробуй еще раз"; goto cgoto5; };
            lgoto5:cout << "Горит ли лампа? ";
            if (!(cin >> lamp)) { cout << "Не то ввел, попробуй еще раз"; goto lgoto5; };

            if (lamp || (sun && not curtain)) cout << "Светло!\n";
            else cout << "Темно!\n";
            break;
        };
        default: {cout << "Не то ввел, чувак\n"; goto chgoto0; }
    }
    system("pause");
}
