#include <iostream>
#include <cmath>

int main() {
    setlocale(0, "");
    using namespace std;


    short int choice;
    cout << "Выберите программу:\n1. Имя\n2. Сложение, вычитание, произведение и деление\n3. Решение уравнений типа kx+b\n4. Решение уравнений типа  ax^2+bx+c=0\n5. Светло или темно в комнате\n";
    cin >> choice;

    switch (choice) {
    case (1): {cout << "Мое имя\n"; break; }
        case (2): {
            double a, b;
            cout << "Введите A и B: ";
            if (!(cin >> a >> b)) { cout << "Не то ввел"; return 0; };
            cout << "Сумма = " << a + b << endl;
            cout << "Разность = " << a - b << endl;
            cout << "Произведение = " << a * b << endl;
            if (b != 0) cout << "Частнoе от деления = " << a / b << endl;
            else cout << "Нельзя делить на 0!\n";
            break;
        }
        case (3): {
            double a, b;
            cout << "Введите A и B: ";
            if (!(cin >> a >> b)) { cout << "Не то ввел"; return 0; };
            if (a != 0) cout << "X = " << -b / a << endl;
            else cout << "Деление на 0!";
            break;
        }
        case (4): {
            double input1, input2, input3;

            cout << "Привет! Я решаю уравнения типа ax^2+bx+c=0 \nВведите 'a' ";
            if (!(cin >> input1)) { cout << "Не то ввел"; return 0; };
            cout << "Отлично! \nТеперь введите 'b' ";
            if (!(cin >> input2)) { cout << "Не то ввел"; return 0; };
            cout << "Превосходно! \nОстался только 'с' ";
            if (!(cin >> input3)) { cout << "Не то ввел"; return 0; };

            if (input1 == 0 && input2 == 0 && input3 == 0) cout << "X любой\n";
            else if ((input1 == 0 && input2 != 0 || input1 != 0 && input2 == 0) && input3 == 0) cout << 0 << endl;
            else if (input1 == 0 && input2 != 0 && input3 != 0) cout << -input3 / input2 << endl;
            else if (input1 != 0 && input2 == 0 && input3 <= 0) cout << sqrt(-input3 / input1) << " " << -sqrt(-input3 / input1) << endl;
            else if (input1 != 0 && input2 != 0 && input3 == 0) cout << input1 / input2 << " " << 0 << endl;
            else if (input1 != 0 && input2 != 0 && input3 != 0) {
                double dis = (input2 * input2) - (4 * input1 * input3);
                if (dis >= 0) {
                    if (dis != 0) cout << ((-input2 + sqrt(dis)) / (2 * input1) > (-input2 - sqrt(dis)) / (2 * input1) ? (-input2 + sqrt(dis)) / (2 * input1) : (-input2 - sqrt(dis)) / (2 * input1)) << " ";
                    cout << ((-input2 + sqrt(dis)) / (2 * input1) < (-input2 - sqrt(dis)) / (2 * input1) ? (-input2 + sqrt(dis)) / (2 * input1) : (-input2 - sqrt(dis)) / (2 * input1)) << endl;
                }
                else cout << "Отрицательный дискриминант!\n";
            }
            else cout << "Нет решения!\n";
            break;
        }
        case (5): {
            bool sun, curtain, lamp;

            cout << "Светло на улице? ";
            if (!(cin >> sun)) { cout << "Не то ввел"; return 0; };
            cout << "Окна зашторены? ";
            if (!(cin >> curtain)) { cout << "Не то ввел"; return 0; };
            cout << "Горит ли лампа? ";
            if (!(cin >> lamp)) { cout << "Не то ввел"; return 0; };

            if (lamp || (sun && not curtain)) cout << "Светло!\n";
            else cout << "Темно!\n";
            break;
        }
        default: cout << "Не то ввел, чувак\n";
    }
    system("pause");
}
